    /// <summary>
    /// يطبع الصفر من بداية الرقم إن وُجد
    /// </summary>
    public static string NormalizeNationalNumber(string phone)
    {
        var digits = new string((phone ?? "").Where(char.IsDigit).ToArray());
        while (digits.Length > 1 && digits[0] == '0')
            digits = digits[1..];
        return digits;
    }

    /// <summary>
    /// يتحقق من صحة الرقم ويعيد الرقم الكامل (مفتاح الدولة + الرقم الوطني) أو رسالة خطأ
    /// </summary>
    /// <param name="countryCode">مفتاح الدولة (مثل 964، 966) بدون +</param>
    /// <param name="phone">الرقم الوطني كما أدخله المستخدم (قد يحتوي 0 في البداية)</param>
    /// <param name="fullPhone">الرقم الكامل بعد التحقق</param>
    /// <param name="errorMessage">رسالة الخطأ إن كان الرقم غير صالح</param>
    /// <returns>true إذا كان الرقم صالحاً</returns>
    public static bool TryValidate(string countryCode, string phone, out string fullPhone, out string errorMessage)
    {
        fullPhone = "";
        errorMessage = "";

        var code = (countryCode ?? "").Trim().TrimStart('+').Replace(" ", "");
        if (string.IsNullOrEmpty(code) || code.Length > 4)
        {
            errorMessage = "مفتاح الدولة غير صالح";
            return false;
        }

        var national = NormalizeNationalNumber(phone ?? "");
        if (string.IsNullOrEmpty(national))
        {
            errorMessage = "رقم الهاتف مطلوب";
            return false;
        }

        if (!national.All(char.IsDigit))
        {
            errorMessage = "رقم الهاتف يجب أن يحتوي على أرقام فقط";
            return false;
        }

        fullPhone = code + national;
        if (fullPhone.Length > 20)
        {
            errorMessage = "رقم الهاتف طويل جداً";
            return false;
        }

        if (Rules.TryGetValue(code, out var rule))
        {
            if (national.Length < rule.Min || national.Length > rule.Max)
            {
                errorMessage = rule.ErrorMsg;
                return false;
            }
        }
        else
        {
            if (national.Length < 7 || national.Length > 15)
            {
                errorMessage = "رقم الهاتف يجب أن يكون بين 7 و 15 رقماً بدون 0 في البداية";
                return false;
            }
        }

        return true;
    }
